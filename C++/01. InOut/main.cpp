#include <iostream>

using namespace std;

int main() {
    // cout 출력 함수 << endl 은 '\n를 의미
    cout << "Hello World"  << endl;
    
    cout << "Input Nuber ";

    int num ; 
    // cin 입력 함수 >> 
    // cin >> num ; num 쪽에 입력하겠다라고 생각
    cin >> num;
    cout << "number : " << num << endl;
    return 0;
}